DROP TABLE IF EXISTS contacts;

CREATE TABLE contacts (
  contactId INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user1_id INTEGER REFERENCES users(userId) ON DELETE SET NULL,
  user2_id INTEGER REFERENCES users(userId) ON DELETE SET NULL,
  blocked BOOLEAN DEFAULT false NOT NULL,
  blocked_by INTEGER REFERENCES users(userId) ON DELETE SET NULL,
  date_connected TIMESTAMPTZ DEFAULT now() NOT NULL
);